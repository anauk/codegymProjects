package tests;

public class Adder {
    // поля значений
    private final int val1;
    private final int val2;
    // конструктор
    public Adder(int val1, int val2) {
        this.val1 = val1;
        this.val2 = val2;
    }
    // метод для получения суммы значений
    public int get(){return val1 +  val2;}


}
